/**
 * Site package loading + GPS auto-detect.
 */
import { state } from './state.js';
import { TEMPLATE_BASE, haversineKm } from './utils.js';

export async function loadSitePackages() {
  const files = ['anse-chastanet.json', 'kittiwake-wreck.json'];
  for (const f of files) {
    try {
      const resp = await fetch(`${TEMPLATE_BASE}/site-packages/${f}`);
      if (resp.ok) state.sitePackages.push(await resp.json());
    } catch {}
  }
}

export function detectSite(lat, lng) {
  for (const site of state.sitePackages) {
    if (haversineKm(lat, lng, site.centerLat, site.centerLng) <= 2) {
      state.currentSite = site;
      return site;
    }
  }
  state.currentSite = null;
  return null;
}
